
public class Resultingdatatypes {

    public static void main(Stringstd[] args) {
        // Increment and decrement operator
        int i = 56;
        // b = i++ then first b is asined with i(56)then it will be incremented
        int j = 67;
        int c = ++j;   // first j will increment 68 then assigned to c 
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        int x = 7;
        int y = ++x * 8;
        System.out.println(y);
        char a = 'B';
        a++;
        System.out.println(a);
    }

}
